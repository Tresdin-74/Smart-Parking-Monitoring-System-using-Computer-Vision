
"""
utils.py
====================================================
توابع کمکی مشترک که در چند بخش پروژه سیستم نظارت پارکینگ استفاده می‌شوند.
این فایل شامل:
 - تابع بهبود کنتراست با CLAHE (مقابله با تغییرات نور)
 - تابع مقایسه هیستوگرام دو تصویر (برای تشخیص تغییر شدید در یک جای پارک)
 - تابع گرفتن timestamp فعلی برای استفاده در لاگ‌ها
"""

# ---------------------------------------------------------------
# وارد کردن کتابخانه‌های مورد نیاز
# ---------------------------------------------------------------
import cv2          # کتابخانه اصلی پردازش تصویر
import datetime     # برای ساخت timestamp


def apply_clahe(gray_image, clip_limit=2.0, tile_grid_size=(8, 8)):
    """
    این تابع الگوریتم CLAHE (Contrast Limited Adaptive Histogram Equalization)
    را روی یک تصویر سطح خاکستری اعمال می‌کند.
    هدف: بهبود کنتراست محلی تصویر تا سیستم در برابر تغییرات نور (مثلاً سایه یا
    نور آفتاب) مقاوم‌تر شود.

    clip_limit: محدودیت کنتراست (هرچه بیشتر باشد، کنتراست بیشتر تقویت می‌شود)
    tile_grid_size: اندازه شبکه‌ای که تصویر برای پردازش محلی به آن تقسیم می‌شود
    """
    # ساخت آبجکت CLAHE با پارامترهای داده‌شده
    clahe = cv2.createCLAHE(clipLimit=clip_limit, tileGridSize=tile_grid_size)

    # اعمال CLAHE روی تصویر سطح خاکستری و برگرداندن نتیجه
    return clahe.apply(gray_image)


def compare_histograms(image1, image2):
    """
    این تابع هیستوگرام دو تصویر سطح خاکستری را با هم مقایسه می‌کند
    و یک عدد بین 0 و 1 برمی‌گرداند (1 یعنی کاملاً مشابه، 0 یعنی کاملاً متفاوت).
    از این تابع برای تأیید اضافی تشخیص تغییر در یک جای پارک استفاده می‌شود
    (در کنار background subtraction) تا flickering کاهش یابد.
    """
    # محاسبه هیستوگرام تصویر اول (256 بین، بازه مقادیر 0 تا 256)
    hist1 = cv2.calcHist([image1], [0], None, [256], [0, 256])

    # محاسبه هیستوگرام تصویر دوم
    hist2 = cv2.calcHist([image2], [0], None, [256], [0, 256])

    # نرمال‌سازی هیستوگرام‌ها تا در بازه قابل مقایسه باشند
    cv2.normalize(hist1, hist1)
    cv2.normalize(hist2, hist2)

    # مقایسه دو هیستوگرام با روش همبستگی (Correlation)
    # خروجی نزدیک به 1 یعنی دو تصویر بسیار مشابه‌اند
    similarity = cv2.compareHist(hist1, hist2, cv2.HISTCMP_CORREL)

    return similarity


def get_timestamp():
    """
    این تابع زمان فعلی سیستم را به‌صورت رشته متنی با فرمت
    'YYYY-MM-DD HH:MM:SS' برمی‌گرداند تا در فایل‌های CSV لاگ ثبت شود.
    """
    # گرفتن زمان فعلی از سیستم
    now = datetime.datetime.now()

    # تبدیل به رشته با فرمت مشخص
    return now.strftime("%Y-%m-%d %H:%M:%S")
